
package co.com.janus.quasar.business.domain.rest;


/**
 * The Class Position.
 * @author Oscar Ortiz
 */
public class Position {

    /** The x coordinate. */
    private Float x;
    
    /** The y coordinate. */
    private Float y;

    /**
     * Gets the x coordinate.
     *
     * @return the x
     */
    public Float getX() {
        return x;
    }

    /**
     * Sets the x coordinate.
     *
     * @param x the new x
     */
    public void setX(Float x) {
        this.x = x;
    }

    /**
     * Gets the y.
     *
     * @return the y
     */
    public Float getY() {
        return y;
    }

    /**
     * Sets the y.
     *
     * @param y the new y
     */
    public void setY(Float y) {
        this.y = y;
    }

}
